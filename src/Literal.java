public class Literal {
    public static void main(String[] args){
        int numero;
        String cadeiaCaractere;
        char caractere;
        boolean certoOuErrado;

        numero = 10;
        cadeiaCaractere = "Igor Carrilho";
        caractere = 's';
        certoOuErrado = true;

        System.out.println("O valor de número é: " + numero);
        System.out.println("O valor de cadeiaCaractere é: " + cadeiaCaractere);
        System.out.println("O valor de caractere é: " + caractere);
        System.out.println("O valor de certoOuErrado é: " + certoOuErrado);
    }
}
